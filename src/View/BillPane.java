    private void validateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validateButtonActionPerformed
        if(control.validateBills(bill)){
            try {
                JOptionPane.showMessageDialog(null, "Checkout validated");
                createBillFile();
                billingView.removeSelectedTab();
                if(control.getAllBills().isEmpty()) billingView.setVisible(false);
            } catch (IOException ex) {
                Logger.getLogger(BillPane.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_validateButtonActionPerformed

    private void createBillFile() throws IOException{
        String date = Calendar.YEAR+"-"+Calendar.MONTH+"-"+Calendar.DAY_OF_MONTH;
        File file = new File("bills/#"+bill.getId()+"-"+date+"-"+Calendar.HOUR_OF_DAY+"-"+Calendar.MINUTE+".txt");
        file.createNewFile();
        writeInformation(file, date);
    }
    
    private void writeInformation(File file, String date) throws IOException{
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            writer.write(" * Bill code: "+bill.getId()+"\n");
            writer.write(" * Costumer's Name: "+bill.getUser().getUserName()+"\n");
            writer.write(" * Payment method: "+bill.getPaymentMethod()+"\n");
            writer.write(" * Date of creation: "+bill.getDateCreation()+"\n");
            writer.write(" * Date of expedition: "+date+"\n");
            writer.write(" * List of products: \n");
            
            for (String product : bill.getProducts()) {
                writer.write(" \t* "+product+" \n");
            }
            writer.write(" * Total Price:"+bill.getTotalPrice()+"â‚¬ \n");
            writer.write(" \t\tÂ¡Â¡Â¡Thank you so much for your shopping!!!\n");
        }
    }

